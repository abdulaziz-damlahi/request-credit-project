                <h3><center>Anında Kredin Finansal Sözleşme</center></h3>
                Krediyi ödeyememe durumunuza karşılık yapılan güvence sigortası bedeli olması sebebi ile özel finans firması olduğumuz için bankalar ve kuruluşları gibi krediden kesinti yapılmamaktadır.<br><br>
                Elde edilen sigorta bedelleri ve faizler ile sizlere kredi hizmeti sunduğumuzu hatırlatmak isteriz.
                <br><br>
                <strong>Finans Departmanı</strong><br>
                Saygılarımızla.
                </p>
