                KİŞİSEL VERİLERİN KORUNMASI AYDINLATMA METNİ
                Kişisel verilerinizin, 6698 sayılı Kişisel Verilerin Korunması Kanunu (“KVKK”) ile özel hayatın
                gizliliği ve kişilerin temel hak ve özgürlüklerinin korunması ilkesine uygun olarak muhafazası,
                işlenmesi ve mevzuata uygun olarak aktarımı aşamalarında AnındaKredin T.A.Ş., olarak veri
                sorumlusu sıfatıyla hareket ederek, gerekli önlemleri alıyoruz. Bu konularda sizleri
                bilgilendirmek isteriz.<br><br>
                KİŞİSEL VERİLERİN TOPLANMASI VE HUKUKİ SEBEP<br><br>
                Anindakredin T.A.Ş., müşterilerinden ve/veya müşterilerinin yetkililerinden kişisel verilerini her türlü
                yazılı, sözlü ve elektronik mecra, üçüncü kişi ve/veya yasal mercilerden aşağıda yer verilen
                amaçlar ve sunduğumuz ürün/hizmetlerin, Bankamızın tabi olduğu yasal düzenlemeler
                çerçevesinde sunulabilmesi ile ilgili kişiler ile kurulan sözleşmeler kapsamındaki
                yükümlülüklerimizin yerine getirebilmesi gayesi veya işleme amacı ile bağlantılı olarak gerekli
                durumlarda ilgili kişilerin açık rızası ile temin edilmektedir. Bu yöntemlerle toplanan kişisel
                verileriniz KVKK’nın kişisel verilerin ve özel nitelikli kişisel verilerin işlenme şartları
                çerçevesinde, aşağıda belirtilen amaçlarla işlenebilmekte ve aktarılabilmektedir.
                KİŞİSEL VERİLERİN İŞLENME AMACI
                Anindakredin T.A.Ş., müşterileri ve müşterilerinin yetkililerinden zorunlu olarak alması gereken
                bilgilerin yanında, yalnızca vermeyi hedeflediği en iyi hizmeti ve ürünü sağlamak amacıyla
                gerekli olduğunu düşündüğü kişisel verileri ve özel nitelikli kişisel verileri, yasal düzenlemelerin
                imkân verdiği ölçüde işlemekte, saklamakta ve aktarmaktadır. Bu bağlamda kişisel verileriniz,
                bankamızda sahip olduğunuz ürün ve hizmetlerin sunulabilmesi, bu ürünlere ilişkin genel
                bilgilendirmelerin yapılabilmesi ve fırsatların iletilebilmesi, acente sıfatıyla sigorta ve diğer her
                tür ürün ve hizmetin sunulması ve bu hizmetlerin iştiraklerimiz ve iş ortaklarımızca
                sürdürülebilmesinin yanı sıra; bankamızın diğer ürün ve hizmetleri ile ilgili olarak tarafınızla
                iletişime geçilmesi, ürün satışı, pazarlama ve bilgilendirme faaliyetlerinde bulunulması, müşteri
                edinimi, müşteri memnuniyeti ve kazandırma çalışmalarının gerçekleştirilmesi ve
                hizmetlerimize ilişkin tekliflerin paylaşılması, hizmet kalitemizin artırılması, risk analizi, müşteri
                ve bankacılık ihtiyaçlarına yönelik geliştirmeler yapılabilmesi, bülten ve davetiye gönderimi,
                analiz, raporlama, segmentasyon ve modelleme çalışmalarının yürütülmesi, banka faaliyetleri
                kapsamında yapılması gerekli görülen raporlamaların, incelemelerin yapılması amaçları
                kapsamında işlenebilmektedir.
                <br><br>İŞLENEN KİŞİSEL VERİLERİN AKTARIMI
                Anindakredin T.A.Ş., kişisel verilerinizi; kamu tüzel kişileri (Örneğin; BDDK, SPK, TCMB, MASAK,
                SGK), özel yetkili kuruluşlar (Örneğin;Kredi Kayıt Bürosu, Bankalararası Kart Merkezi, Türkiye
                Bankalar Birliği), yurtiçi ve/veya yurtdışındaki; ana hissedarımız, iştiraklerimiz, bankacılık
                faaliyetlerini yürütmek üzere hizmet aldığımız, işbirliği yaptığımız, program ortağı kuruluşlar,
                bankalar, finansal kuruluşlar, bağımsız denetim şirketleri ile, tabi olunan mevzuat hükümlerinin
                izin verdiği ölçüde paylaşabilir.
                Anindakredin T.A.Ş.’nin müşterileri ile gerçekleştirdiği işlemlere ilişkin kayıt ve belgeleri yasal
                düzenlemeler kapsamında belirli bir süre ile saklaması söz konusu olup; kişisel verilerinizin
                silinmesini veya yok edilmesini ya da anonimleştirilmesini istemeniz halinde bu talebiniz yasal
                düzenlemeler ile belirlenen süre sonunda yerine getirilebilecek; bu süre zarfında kişisel
                verileriniz ulusal ve uluslararası yasa, düzenleme ve sözleşmelerden kaynaklı zorunluluklar
                haricinde işlenmeyecek ve 3. kişiler ile paylaşılmayacaktır.
                Anindakredin T.A.Ş. Genel Müdürlük: Sabancı Center 4.Levent 34330 İstanbul, MERSİS No: 0015 0015 2340 0087, www.anindakredin.com
                KİŞİSEL VERİSİ İŞLENEN İLGİLİ KİŞİNİN HAKLARI
                KVKK hükümleri uyarınca, Anindakredin T.A.Ş.’ye başvurarak aşağıdaki haklarınızı
                kullanabilirsiniz:<br><br>
                 Kişisel veri işlenip işlenmediğini öğrenme,
                 Kişisel verileri işlenmişse buna ilişkin bilgi talep etme,
                 Kişisel verilerin işlenme amacını ve bunların amacına uygun kullanılıp kullanılmadığını
                öğrenme,
                 Yurt içinde veya yurt dışında kişisel verilerin aktarıldığı üçüncü kişileri bilme,
                 Kişisel verilerin eksik veya yanlış işlenmiş olması hâlinde bunların düzeltilmesini
                isteme,
                 Kişisel verilerin silinmesini veya yok edilmesini isteme,
                 Kişisel verilerin düzeltilmesi, silinmesi ya da yok edilmesi halinde bu işlemlerin kişisel
                verilerin aktarıldığı üçüncü kişilere bildirilmesini isteme,
                 İşlenen verilerin münhasıran otomatik sistemler vasıtasıyla analiz edilmesi suretiyle
                kişinin kendisi aleyhine bir sonucun ortaya çıkmasına itiraz etme,
                 Kişisel verilerin kanuna aykırı olarak işlenmesi sebebiyle zarara uğraması hâlinde
                zararın giderilmesini talep etme.
                Bu kapsamda iletilen talepler Anindakredin T.A.Ş. tarafından en geç otuz gün içinde ücretsiz olarak
                sonuçlandırılacaktır. Ancak, Kişisel Verileri Koruma Kurulu’nca bir ücret öngörülmesi halinde,
                bankamız tarafından belirlenen tarifedeki ücret alınabilir.
